        return UIColor(red: 255.0 / 255.0, green: 155.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var warmGrey: UIColor {
        return UIColor(red: 121.0 / 255.0, green: 121.0 / 255.0, blue: 121.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var windowsBlue: UIColor {
        return UIColor(red: 56.0 / 255.0, green: 133.0 / 255.0, blue: 199.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var textBlack: UIColor {
        return UIColor(red: 51.0 / 255.0, green: 51.0 / 255.0, blue: 51.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
